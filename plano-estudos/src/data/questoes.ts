/** Retorna as questões cujo tópico está entre os tópicos informados. */
export function questoesPorTopicos(topicos: string[]): Questao[] {
  if (!topicos.length) return [];
  const set = new Set(topicos);
  return questoes.filter((q) => set.has(q.topico));
}

export function contarPorTopico(topico: string): number {
  return questoes.filter((q) => q.topico === topico).length;
}
